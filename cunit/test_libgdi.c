	CU_ASSERT(gdi_EqualRgn(invalid, rgn2) == 1);

	/* top outside */
	gdi_SetRgn(invalid, 300, 300, 100, 100);
	gdi_SetRgn(rgn1, 300, 100, 100, 300);
	gdi_SetRgn(rgn2, 300, 100, 100, 300);

	gdi_InvalidateRegion(hdc, rgn1->x, rgn1->y, rgn1->w, rgn1->h);
	CU_ASSERT(gdi_EqualRgn(invalid, rgn2) == 1);

	/* bottom outside */
